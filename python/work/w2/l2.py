# Приклади арифметичних операцій
a = 10
b = 3

sum = a + b          # 13
difference = a - b   # 7
product = a * b      # 30
quotient = a / b     # 3.333...
remainder = a % b    # 1
power = a ** b       # 1000

# Виведення результатів
print("Сума:", sum)
print("Різниця:", difference)
print("Добуток:", product)
print("Частка:", quotient)
print("Залишок:", remainder)
print("Ступінь:", power)